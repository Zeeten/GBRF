package com.ncs.bean;

public class InitialAwardCorpusBean extends BaseBean {

	private int amount;
	private int noOfAward;
	private int totalAmount;
	private int part;

	public int getAmount() {
		return amount;
	}

	public void setAmount(int amount) {
		this.amount = amount;
	}

	public int getNoOfAward() {
		return noOfAward;
	}

	public void setNoOfAward(int noOfAward) {
		this.noOfAward = noOfAward;
	}

	public int getTotalAmount() {
		return totalAmount;
	}

	public void setTotalAmount(int totalAmount) {
		this.totalAmount = totalAmount;
	}

	public int getPart() {
		return part;
	}

	public void setPart(int part) {
		this.part = part;
	}
}
